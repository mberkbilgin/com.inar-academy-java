package chapters.chapter06.Checkpoints;

public class Checkpoint06_12 {
    public static void main(String[] args) {
        nPrintln("Welcome to Java!",5);
    }
    public static void nPrintln(String str, int n){
        for (int i = 0; i < n; i++) {
            System.out.println(str);
        }
    }
}
